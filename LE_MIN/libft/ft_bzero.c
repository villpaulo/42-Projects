#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptrcpy;

	if (n == 0)
		return ;
	i = 0;
	ptrcpy = s;
	while (i < n)
		ptrcpy[i++] = 0;
}
