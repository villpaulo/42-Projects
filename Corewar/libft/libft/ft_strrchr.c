#include "./../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*s2;

	i = 0;
	s2 = (char*)s;
	while (s2[i] != '\0')
		i++;
	while (s2[i] != c && i > 0)
		i--;
	if (s2[i] == c)
		return (&s2[i]);
	return (NULL);
}
