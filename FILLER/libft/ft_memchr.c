#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*t;

	i = 0;
	t = (unsigned char *)s;
	while (t && i < n && t[i] != (unsigned char)c)
		i++;
	if (i == n)
		return (NULL);
	return (&t[i]);
}
